public class BIneryOperator {
    public static void main(String[] args) {
        int x = 10;
        // pre-Increment
        System.out.println(++x);
        // post-increment
        System.out.println(x++);


        int y = 10;
        // pre-Decrement
        System.out.println(--y);
        // post-Decrement
        System.out.println(y--);

        // logical NOT
        boolean isTrue = true;
        System.out.println(isTrue);
    }
}